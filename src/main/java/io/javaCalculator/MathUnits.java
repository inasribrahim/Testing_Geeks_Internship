package io.javaCalculator;

public class MathUnits {
	public double addTwoNumber(double number1 , double number2) {return number1 + number2 ;  }
	
	public double subTwoNumber(double number1 , double number2) {return number1 - number2 ; } 
	
	public int  divTwoNumber(int number1 , int number2) {return number1 / number2;}
	
	public double multTwoNumber(double number1 , double number2){return number1 * number2 ; }
}
